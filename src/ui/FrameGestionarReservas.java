    /**
     * Crea una nueva reserva con el cliente y servicio seleccionados, validando
     * la cantidad de integrantes y los cupos disponibles del servicio.
     */
    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        Cliente cliente = obtenerClienteElegido();
        ServicioTuristico servicio = obtenerServicioElegido();

        if (isNull(cliente) || isNull(servicio)) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un cliente y un servicio", "Atención", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (gestor.reservasLlenas()) {
            JOptionPane.showMessageDialog(this, "Se alcanzó el máximo de 40 reservas permitidas", "Atención", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String integrantes = txtIntegrantesCrear.getText().strip();

        if (!utilReserva.esEntero(integrantes) || !utilReserva.validarCantidadIntegrantes(integrantes)) {
            return;
        }

        int cantidad = Integer.parseInt(integrantes);
        int cupos = gestor.cuposDisponibles(servicio, null);
        if (cantidad > cupos) {
            JOptionPane.showMessageDialog(this, "El servicio solo tiene " + cupos + " cupos disponibles", "Sin cupos", JOptionPane.WARNING_MESSAGE);
            return;
        }

        //La reserva se crea con el código que tiene asignado el cliente elegido
        Reserva reserva = new Reserva(cliente.getCodigoReserva(), cliente, servicio, integrantes);
        gestor.agregarReserva(reserva);
        JOptionPane.showMessageDialog(this, "Reserva creada con éxito", "Correcto", JOptionPane.INFORMATION_MESSAGE);
        txtIntegrantesCrear.setText("");
        actualizarCuposCrear();
    }//GEN-LAST:event_btnCrearActionPerformed

    /**
     * Busca las reservas según el criterio y el texto ingresados, y muestra la
     * primera coincidencia. Si hay más de un resultado, habilita las flechas de
     * navegación e informa la cantidad encontrada.
     */
    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String criterio = comboBuscar.getSelectedItem().toString();
        String texto = txtBuscar.getText().strip().trim();

        if (texto.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe ingresar un valor para buscar", "Atención", JOptionPane.WARNING_MESSAGE);
            return;
        }

        ArrayList<Reserva> encontradas;
        if (criterio.equals("Código Reserva")) {
            encontradas = gestor.buscarReservasXCodigo(texto);
        } else {
            encontradas = gestor.buscarReservasXNombreCliente(texto);
        }

        if (encontradas.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No se encontró ninguna reserva\ncon los datos proporcionados", "Sin resultados", JOptionPane.INFORMATION_MESSAGE);
            limpiar();
            return;
        }

        resultadosBusqueda = encontradas;
        indiceResultado = 0;
        mostrarResultadoActual();

        if (encontradas.size() > 1) {
            JOptionPane.showMessageDialog(this, "Se encontraron " + encontradas.size() + " reservas, utilice las flechas para navegar.", "Resultados encontrados", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    /**
     * Retrocede al resultado anterior de la búsqueda actual, volviendo al
     * último resultado si ya se estaba en el primero.
     */
    private void btnAnteriorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnteriorActionPerformed
        if (resultadosBusqueda.isEmpty()) {
            return;
        }
        indiceResultado = (indiceResultado - 1 + resultadosBusqueda.size()) % resultadosBusqueda.size();
        mostrarResultadoActual();
    }//GEN-LAST:event_btnAnteriorActionPerformed

    /**
     * Avanza al siguiente resultado de la búsqueda actual, volviendo al primer
     * resultado si ya se estaba en el último.
     */
    private void btnSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSiguienteActionPerformed
        if (resultadosBusqueda.isEmpty()) {
            return;
        }
        indiceResultado = (indiceResultado + 1) % resultadosBusqueda.size();
        mostrarResultadoActual();
    }//GEN-LAST:event_btnSiguienteActionPerformed

    /**
     * Actualiza la cantidad de integrantes de la reserva seleccionada,
     * respetando los cupos disponibles del servicio asociado.
     */
    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        if (isNull(reservaActual)) {
            JOptionPane.showMessageDialog(this, "Primero debe buscar una reserva", "Atención", JOptionPane.WARNING_MESSAGE);
            return;
        }
        String integrantes = txtIntegrantesSel.getText().strip();
        if (!utilReserva.esEntero(integrantes) || !utilReserva.validarCantidadIntegrantes(integrantes)) {
            return;
        }
        int cantidad = Integer.parseInt(integrantes);
        int cuposMax = gestor.cuposDisponibles(reservaActual.getServicio(), reservaActual);
        if (cantidad > cuposMax) {
            JOptionPane.showMessageDialog(this, "El servicio solo admite " + cuposMax + " integrantes para esta reserva", "Sin cupos", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Reserva actualizada = new Reserva(reservaActual.getCodigoReserva(), reservaActual.getCliente(), reservaActual.getServicio(), integrantes);
        gestor.actualizarReserva(actualizada, indiceActual);
        reservaActual = actualizada;
        resultadosBusqueda.set(indiceResultado, actualizada);
        JOptionPane.showMessageDialog(this, "Reserva actualizada con éxito", "Correcto", JOptionPane.INFORMATION_MESSAGE);
        actualizarCuposCrear();
    }//GEN-LAST:event_btnActualizarActionPerformed

    /**
     * Elimina la reserva seleccionada previa confirmación del usuario.
     */
    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (isNull(reservaActual)) {
            JOptionPane.showMessageDialog(this, "Primero debe buscar una reserva", "Atención", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int respuesta = JOptionPane.showConfirmDialog(this, "¿Está seguro de eliminar la reserva seleccionada?", "Confirmar eliminación", JOptionPane.YES_NO_OPTION);
        if (respuesta == JOptionPane.YES_OPTION) {
            gestor.eliminarReserva(indiceActual);
            JOptionPane.showMessageDialog(this, "Reserva eliminada con éxito", "Correcto", JOptionPane.INFORMATION_MESSAGE);
            limpiar();
            actualizarCuposCrear();
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

