    /**
     * Carga los combos de clientes y servicios desde el gestor. Los combos solo
     * muestran texto, por lo que además se guardan las listas de objetos para
     * poder recuperar el cliente o el servicio elegido según su posición.
     * Si falta alguno, deshabilita la creación de reservas.
     */
    private void cargarComboBox() {
        comboBoxCliente.removeAllItems();
        comboBoxServicio.removeAllItems();
        clientes = gestor.obtenerClientes();
        servicios = gestor.obtenerServicios();

        for (Cliente cliente : clientes) {
            comboBoxCliente.addItem(cliente.getNombre() + " - " + cliente.getRut().toString());
        }
        for (ServicioTuristico servicio : servicios) {
            comboBoxServicio.addItem(servicio.getNombre() + " (cap. " + servicio.getCapacidad() + ")");
        }

        boolean puedeCrear = comboBoxCliente.getItemCount() > 0 && comboBoxServicio.getItemCount() > 0;
        btnCrear.setEnabled(puedeCrear);
        if (!puedeCrear) {
            labelCuposCrear.setText("Debe existir al menos un cliente y un servicio");
        }
    }

    /**
     * Obtiene el cliente que está elegido en el combo, usando su posición.
     * @return el cliente seleccionado, o null si el combo está vacío.
     */
    private Cliente obtenerClienteElegido() {
        int posicion = comboBoxCliente.getSelectedIndex();
        if (posicion < 0) {
            return null;
        }
        return clientes.get(posicion);
    }

    /**
     * Obtiene el servicio que está elegido en el combo, usando su posición.
     * @return el servicio seleccionado, o null si el combo está vacío.
     */
    private ServicioTuristico obtenerServicioElegido() {
        int posicion = comboBoxServicio.getSelectedIndex();
        if (posicion < 0) {
            return null;
        }
        return servicios.get(posicion);
    }

    /**
     * Muestra en el formulario el código de reserva del cliente elegido, ya que
     * la reserva se crea con el código que ese cliente tiene asignado.
     */
    private void mostrarCodigoCliente() {
        Cliente cliente = obtenerClienteElegido();
        if (isNull(cliente)) {
            txtCodigoCrear.setText("");
            return;
        }
        txtCodigoCrear.setText(cliente.getCodigoReserva());
    }

    /**
     * Actualiza la etiqueta de cupos disponibles del servicio seleccionado en la creación.
     */
    private void actualizarCuposCrear() {
        ServicioTuristico servicio = obtenerServicioElegido();
        if (isNull(servicio)) {
            return;
        }
        int cupos = gestor.cuposDisponibles(servicio, null);
        labelCuposCrear.setText(cupos + " de " + servicio.getCapacidad());
    }

    /**
     * Muestra en el formulario la reserva ubicada en indiceResultado dentro de
     * resultadosBusqueda, y actualiza el estado de las flechas de navegación.
     */
    private void mostrarResultadoActual() {
        reservaActual = resultadosBusqueda.get(indiceResultado);
        indiceActual = gestor.indiceDeReserva(reservaActual);

        labelCodigoSel.setText(reservaActual.getCodigoReserva());
        labelClienteSel.setText(reservaActual.getCliente().getNombre() + " (" + reservaActual.getCliente().getRut().toString() + ")");
        labelServicioSel.setText(reservaActual.getServicio().getNombre());
        int cuposMax = gestor.cuposDisponibles(reservaActual.getServicio(), reservaActual);
        labelCuposSel.setText("Máximo " + cuposMax + " (capacidad " + reservaActual.getServicio().getCapacidad() + ")");
        txtIntegrantesSel.setText(String.valueOf(reservaActual.getCantidadIntegrantes()));

        actualizarInfoFrame();
    }

    /**
     * Muestra u oculta las flechas de navegación y el contador de resultados
     * según la cantidad de coincidencias de la última búsqueda: solo se
     * muestran cuando hay más de una reserva encontrada.
     */
    private void actualizarInfoFrame() {
        boolean hayVarios = resultadosBusqueda.size() > 1;
        btnAnterior.setVisible(hayVarios);
        btnSiguiente.setVisible(hayVarios);
        labelResultado.setVisible(hayVarios);
        if (hayVarios) {
            labelResultado.setText((indiceResultado + 1) + " / " + resultadosBusqueda.size());
        }
        pack();
    }

    /**
     * Restablece la selección actual y la navegación entre resultados.
     */
    private void limpiar() {
        reservaActual = null;
        indiceActual = -1;
        resultadosBusqueda = new ArrayList<>();
        indiceResultado = -1;
        labelCodigoSel.setText("-");
        labelClienteSel.setText("-");
        labelServicioSel.setText("-");
        labelCuposSel.setText("-");
        txtIntegrantesSel.setText("");
        btnAnterior.setVisible(false);
        btnSiguiente.setVisible(false);
        labelResultado.setVisible(false);
    }

