    /**
     * Rellena el formulario con los datos del cliente o guía hallado y muestra
     * únicamente los campos correspondientes a una persona.
     * @param persona: persona encontrada a mostrar.
     */
    private void mostrarPersona(Persona persona) {
        txtNombre.setText(persona.getNombre());
        txtEdad.setText(String.valueOf(persona.getEdad()));
        txtRut.setText(persona.getRut().toString());

        Direccion direccion = persona.getDireccion();
        txtCalle.setText(direccion.getCalle());
        txtNumero.setText(String.valueOf(direccion.getNumero()));
        txtComuna.setText(direccion.getComuna());
        txtCiudad.setText(direccion.getCiudad());

        if (persona instanceof Cliente) {
            labelTipo.setText("Tipo: Cliente");
            labelVariable.setText("Cód. Reserva");
            txtVariable.setText(((Cliente) persona).getCodigoReserva());
            //El código de reserva se conserva, no se edita manualmente
            txtVariable.setEditable(false);
        } else if (persona instanceof GuiaTuristico) {
            labelTipo.setText("Tipo: Guía Turístico");
            labelVariable.setText("Especialidad");
            txtVariable.setText(((GuiaTuristico) persona).getEspecialidad());
            txtVariable.setEditable(true);
        }

        mostrarCamposPersona();
    }

    /**
     * Rellena el formulario con los datos del servicio hallado y muestra
     * únicamente los campos correspondientes a un servicio, adaptando el campo
     * variable al subtipo.
     * @param servicio: servicio turístico encontrado a mostrar.
     */
    private void mostrarServicio(ServicioTuristico servicio) {
        txtNombre.setText(servicio.getNombre());
        txtDuracion.setText(String.valueOf(servicio.getDuracionHoras()));
        txtCapacidad.setText(String.valueOf(servicio.getCapacidad()));
        txtVariable.setEditable(true);

        if (servicio instanceof RutaGastronomica) {
            labelTipo.setText("Tipo: Ruta Gastronómica");
            labelVariable.setText("Nro. Paradas");
            txtVariable.setText(String.valueOf(((RutaGastronomica) servicio).getNumeroDeParadas()));
        } else if (servicio instanceof PaseoLacustre) {
            labelTipo.setText("Tipo: Paseo Lacustre");
            labelVariable.setText("Tipo Embarcación");
            txtVariable.setText(((PaseoLacustre) servicio).getTipoEmbarcacion());
        } else if (servicio instanceof ExcursionCultural) {
            labelTipo.setText("Tipo: Excursión Cultural");
            labelVariable.setText("Lugar Histórico");
            txtVariable.setText(((ExcursionCultural) servicio).getLugarHistorico());
        }

        mostrarCamposServicio();
    }

    /**
     * Valida los campos de persona y devuelve una Dirección válida, o null si
     * algún dato es incorrecto (los validadores ya informan el error).
     * @return la Dirección construida, o null si la validación falla.
     */
    private Direccion validarDatosPersona() {
        String nombre = txtNombre.getText().strip();
        String edad = txtEdad.getText().strip();
        String calle = txtCalle.getText().strip();
        String numero = txtNumero.getText().strip();
        String comuna = txtComuna.getText().strip();
        String ciudad = txtCiudad.getText().strip();
        String rut = txtRut.getText().strip();

        if (!utilPersona.validarNombre(nombre)) {
            return null;
        }
        if (!utilPersona.esEntero(edad) || !utilPersona.validarEdad(edad)) {
            return null;
        }
        if (!utilDireccion.validarCalle(calle)) {
            return null;
        }
        if (!utilDireccion.esEntero(numero) || !utilDireccion.validarNumeracion(numero)) {
            return null;
        }
        if (!utilDireccion.validarComuna(comuna)) {
            return null;
        }
        if (!utilDireccion.validarCiudad(ciudad)) {
            return null;
        }
        if (!utilRut.validarRut(rut)) {
            return null;
        }
        return new Direccion(calle, numero, comuna, ciudad);
    }

    /**
     * Reconstruye y actualiza un cliente conservando su código de reserva.
     */
    private void actualizarCliente() {
        Direccion direccion = validarDatosPersona();
        if (isNull(direccion)) {
            return;
        }
        String codigo = txtVariable.getText().strip();
        if (!utilCliente.validarCodigo(codigo)) {
            return;
        }
        Rut rut = new Rut(txtRut.getText().strip().strip());
        Cliente cliente = new Cliente(txtNombre.getText().strip(), txtEdad.getText().strip(), direccion, rut, codigo);
        gestor.actualizar(cliente, indiceActual);
        entidadActual = cliente;
        JOptionPane.showMessageDialog(this, "Cliente actualizado con éxito", "Correcto", JOptionPane.INFORMATION_MESSAGE);
    }

    /**
     * Reconstruye y actualiza un guía turístico validando su especialidad.
     */
    private void actualizarGuia() {
        Direccion direccion = validarDatosPersona();
        if (isNull(direccion)) {
            return;
        }
        String especialidad = txtVariable.getText().strip();
        if (!utilGuia.validarEspecialidad(especialidad)) {
            return;
        }
        Rut rut = new Rut(txtRut.getText().strip());
        GuiaTuristico guia = new GuiaTuristico(txtNombre.getText().strip(), txtEdad.getText().strip(), direccion, rut, especialidad);
        gestor.actualizar(guia, indiceActual);
        entidadActual = guia;
        JOptionPane.showMessageDialog(this, "Guía actualizado con éxito", "Correcto", JOptionPane.INFORMATION_MESSAGE);
    }

    /**
     * Reconstruye y actualiza un servicio turístico según su subtipo real.
     */
    private void actualizarServicio() {
        String nombre = txtNombre.getText().strip();
        String duracion = txtDuracion.getText().strip();
        String capacidad = txtCapacidad.getText().strip();
        String variable = txtVariable.getText().strip();

        if (!utilServicio.esDouble(duracion)) {
            return;
        }
        if (!utilServicio.esEntero(capacidad) || !utilServicio.validarCapacidad(capacidad)) {
            return;
        }

        ServicioTuristico nuevo;
        if (entidadActual instanceof RutaGastronomica) {
            if (!utilServicio.esEntero(variable)) {
                return;
            }
            nuevo = new RutaGastronomica(nombre, duracion, capacidad, variable);
        } else if (entidadActual instanceof PaseoLacustre) {
            nuevo = new PaseoLacustre(nombre, duracion, capacidad, variable);
        } else {
            nuevo = new ExcursionCultural(nombre, duracion, capacidad, variable);
        }

        if (!utilServicio.validarServicio(nuevo)) {
            return;
        }
        gestor.actualizar(nuevo, indiceActual);
        entidadActual = nuevo;
        JOptionPane.showMessageDialog(this, "Servicio actualizado con éxito", "Correcto", JOptionPane.INFORMATION_MESSAGE);
    }

