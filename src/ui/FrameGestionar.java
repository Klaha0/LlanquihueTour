    /**
     * Busca la entidad según el criterio y el texto ingresados, y muestra la
     * primera coincidencia en el formulario correspondiente. Si la búsqueda por
     * nombre encuentra más de una entidad, habilita las flechas de navegación
     * e informa la cantidad de resultados encontrados.
     */
    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String criterio = comboBoxBuscar.getSelectedItem().toString();
        String texto = txtBuscar.getText().strip().trim();

        if (texto.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe Ingresar un valor para buscar", "Atención", JOptionPane.WARNING_MESSAGE);
            return;
        }

        ArrayList<Persistible> encontradas;
        if (criterio.equals("Código Reserva")) {
            encontradas = comoLista(gestor.buscarXCodigoReserva(texto));
        } else if (criterio.equals("Rut")) {
            encontradas = comoLista(gestor.buscarXRut(texto));
        } else {
            encontradas = gestor.buscarTodosXNombre(texto);
        }

        if (encontradas.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No se encontró ninguna entidad\ncon los datos proporcionados", "Sin resultados", JOptionPane.INFORMATION_MESSAGE);
            limpiarSeleccion();
            return;
        }

        resultadosBusqueda = encontradas;
        indiceResultado = 0;
        mostrarResultadoActual();

        if (encontradas.size() > 1) {
            JOptionPane.showMessageDialog(this, "Se han encontrado " + encontradas.size() + " entidades con ese nombre, utilice las flechas para navegar.", "Resultados encontrados", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    /**
     * Envuelve un resultado de búsqueda exacta (Rut o Código Reserva) en una
     * lista, para poder reutilizar el mismo flujo de navegación que la
     * búsqueda por nombre.
     * @param entidadEncontrada: entidad encontrada, o null si no hubo coincidencia.
     * @return una lista con la entidad, o vacía si encontrada es null.
     */
    private ArrayList<Persistible> comoLista(Persistible entidadEncontrada) {
        var lista = new ArrayList<Persistible>();
        if (entidadEncontrada != null) {
            lista.add(entidadEncontrada);
        }
        return lista;
    }

    /**
     * Retrocede al resultado anterior de la búsqueda actual, volviendo al
     * último resultado si ya se estaba en el primero.
     */
    private void btnAnteriorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnteriorActionPerformed
        indiceResultado = (indiceResultado - 1 + resultadosBusqueda.size()) % resultadosBusqueda.size();
        mostrarResultadoActual();
    }//GEN-LAST:event_btnAnteriorActionPerformed

    /**
     * Avanza al siguiente resultado de la búsqueda actual, volviendo al
     * primer resultado si ya se estaba en el último.
     */
    private void btnSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSiguienteActionPerformed
        indiceResultado = (indiceResultado + 1) % resultadosBusqueda.size();
        mostrarResultadoActual();
    }//GEN-LAST:event_btnSiguienteActionPerformed

    /**
     * Muestra en el formulario el resultado ubicado en indiceResultado dentro
     * de resultadosBusqueda, y actualiza el estado de las flechas de navegación.
     */
    private void mostrarResultadoActual() {
        Persistible entidadEncontrada = resultadosBusqueda.get(indiceResultado);
        entidadActual = entidadEncontrada;
        indiceActual = gestor.indiceDe(entidadEncontrada);

        if (entidadEncontrada instanceof Persona) {
            mostrarPersona((Persona) entidadEncontrada);
        } else if (entidadEncontrada instanceof ServicioTuristico) {
            mostrarServicio((ServicioTuristico) entidadEncontrada);
        }

        actualizarNavegacion();
    }

    /**
     * Muestra u oculta las flechas de navegación y el contador de resultados
     * según la cantidad de coincidencias de la última búsqueda: solo se
     * muestran cuando hay más de una entidad encontrada.
     */
    private void actualizarNavegacion() {
        boolean hayVarios = resultadosBusqueda.size() > 1;
        btnAnterior.setVisible(hayVarios);
        btnSiguiente.setVisible(hayVarios);
        labelResultado.setVisible(hayVarios);
        if (hayVarios) {
            labelResultado.setText((indiceResultado + 1) + " / " + resultadosBusqueda.size());
        }
        pack();
    }

    /**
     * Reconstruye la entidad seleccionada con los datos del formulario y la
     * actualiza en el gestor. Esto según el tipo real de la entidad.
     */
    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        if (isNull(entidadActual)) {
            JOptionPane.showMessageDialog(this, "Primero debe buscar una entidad", "Atención", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (entidadActual instanceof Cliente) {
            actualizarCliente();
        } else if (entidadActual instanceof GuiaTuristico) {
            actualizarGuia();
        } else if (entidadActual instanceof ServicioTuristico) {
            actualizarServicio();
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    /**
     * Elimina la entidad seleccionada previa confirmación del usuario.
     */
    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (isNull(entidadActual)) {
            JOptionPane.showMessageDialog(this, "Primero debe buscar una entidad", "Atención", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int respuesta = JOptionPane.showConfirmDialog(this, "¿Está seguro de eliminar la entidad seleccionada?", "Confirmar eliminación", JOptionPane.YES_NO_OPTION);
        if (respuesta == JOptionPane.YES_OPTION) {
            gestor.eliminar(indiceActual);
            JOptionPane.showMessageDialog(this, "Entidad eliminada con éxito", "Correcto", JOptionPane.INFORMATION_MESSAGE);
            limpiarSeleccion();
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

